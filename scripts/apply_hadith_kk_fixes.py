def main() -> int:
    if not BUNDLE.is_file():
        print(f"Жоқ: {BUNDLE}", file=sys.stderr)
        return 1
    data = json.loads(BUNDLE.read_text(encoding="utf-8"))
    hadiths = data.get("hadiths")
    if not isinstance(hadiths, list):
        print("hadiths тізім емес", file=sys.stderr)
        return 1
    by_id = {str(h.get("id")): h for h in hadiths if isinstance(h, dict) and h.get("id")}
    n = 0
    for hid, new_kk in FIXES.items():
        row = by_id.get(hid)
        if not row:
            print(f"ескерту: id жоқ {hid}", file=sys.stderr)
            continue
        old = str(row.get("textKk") or "")
        if old.strip() == new_kk.strip():
            continue
        row["textKk"] = new_kk
        n += 1
    prov = data.setdefault("provenance", {})
    if isinstance(prov, dict):
        note = str(prov.get("evidenceKk") or "")
        tag = " text_kk түзету: apply_hadith_kk_fixes.py (қате LLM/иснад)."
        if tag.strip() not in note:
            prov["evidenceKk"] = (note + tag).strip()
    BUNDLE.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"Жаңартылды: {n} жол ({BUNDLE})")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
