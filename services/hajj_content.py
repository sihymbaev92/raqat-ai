"""
Қажылық бөлімі мәтіндері: жалпы ақпарат; нақты үкім мәзһаб пен ұстазбен расталуы керек.
"""
from __future__ import annotations

_MAX_MSG = 4000

_FOOTER = (
    "\n\n⚠️ <i>Бұл — жалпы түсінік. Нақты фиқһ, денсаулық, виза және хадж ұйымының "
    "нұсқауларын міндетті түрде ұстаныңыз.</i>"
)


def _clip(s: str) -> str:
    if len(s) <= _MAX_MSG:
        return s
    return s[: _MAX_MSG - 50] + "\n\n<i>…мәтін қысқартылды.</i>"


def build_hajj_text(section: str) -> str:
    builders = {
        "overview": _text_overview,
        "ihram": _text_ihram,
        "talbiyah": _text_talbiyah,
        "d8": _text_d8,
        "d9_arafah": _text_d9,
        "d10_13": _text_d10_13,
        "tawaf_ziyarah_sai": _text_tawaf_sai,
        "jamarat_nahr": _text_jamarat,
        "wida": _text_wida,
        "umrah_full": _text_umrah,
        "women": _text_women,
        "health": _text_health,
        "mistakes": _text_mistakes,
    }
    fn = builders.get(section)
    if not fn:
        return _clip("<b>Бөлім табылмады.</b>" + _FOOTER)
    return _clip(fn() + _FOOTER)


