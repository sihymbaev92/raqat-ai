    intro = (
        "Сурет немесе схема дәрет қимылдарын көрсетеді. Төмендегі ретті жаттауға ыңғайлы "
        "нұсқа ретінде қолданыңыз."
    )
    if target == "men":
        title = "👨 Ер кісінің дәреті"
        intro = (
            "Ер адам үшін дәреттің парыз-сүннеттері жалпы ережелер бойынша. "
            "Сақал, киім, мешітке бару әдебі — қосымша сүннеттер мен мәзһабтық ерекшеліктер "
            "бар; нақтысын ілім көзінен қараңыз."
        )
    elif target == "women":
        title = "🧕 Әйел кісінің дәреті"
