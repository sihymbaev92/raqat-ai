def build_prayer_text(section: str, prayer_times: Optional[dict[str, Any]] = None) -> str:
    if section == "times":
        if prayer_times:
            return _format_times_block(prayer_times, api_ok=True)
        times = get_demo_prayer_times()
        return _format_times_block(times, api_ok=False)

    if section in {"purification", "wudu", "wudu_types", "ghusl", "tayammum"}:
        return _purification_text()

    if section == "wudu_visual":
        return _wudu_visual_combined()

    if section == "visual_wudu":
        return _visual_wudu_text()

    if section == "wudu_men":
        return _visual_wudu_text("men")

    if section == "wudu_women":
        return _visual_wudu_text("women")

    if section == "conditions":
        return _text_conditions()

    if section == "wudu_rules":
        return _wudu_rules_text()

    if section == "salah_2rakat":
        return _text_salah_2rakat()

    if section == "salah_duas":
        return _text_salah_duas_surahs()

    if section == "steps":
        return _text_steps()

    if section == "visual_salah":
        return _visual_salah_text()

    if section == "men":
        return _men_prayer_text()

    if section == "women":
        return _women_prayer_text()

    if section == "types":
        return _text_types()

    if section == "fard":
        return _text_fard()

    if section == "wajib":
        return _text_wajib()

    if section == "sunnah":
        return _text_sunnah()

    if section == "tahajjud":
        return _text_tahajjud()

    if section == "witr":
        return _text_witr()

    if section == "extra":
        return _text_extra()

    if section == "invalidators":
        return _text_invalidators()

    if section == "mistakes":
        return _text_mistakes()

    if section == "travel":
        return _text_travel()

    if section == "jumuah":
        return _text_jumuah()

    return _clip(
        "🕌 <b>Намаз бөлімі</b>\n\n"
        "Төмендегі бөлімдердің бірін таңдаңыз."
    )
