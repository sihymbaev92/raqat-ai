def _format_times_block(times: dict[str, Any], api_ok: bool) -> str:
    src = times.get("source", "")
    method = times.get("method")
    cache_status = times.get("cache_status")
    checked_at = times.get("checked_at_utc")
    extra = ""
    if api_ok and src:
        extra = f"\nДереккөз: <i>{src}</i>"
        if method is not None:
            extra += f" · әдіс №<code>{method}</code>"
        if checked_at:
            extra += f"\nТексерілгені: <i>{checked_at}</i>"
    if api_ok and cache_status == "stale":
        foot = (
            "\n\n⚠️ <b>Соңғы сақталған уақыт</b> — API уақытша қолжетімсіз, "
            "кэштегі соңғы мән көрсетілді."
        )
    elif api_ok and cache_status == "cache":
        foot = "\n\n⚡ <b>Жуықтағы кэш</b> — бірдей сұраныс үшін API-ге қайта жүгінілмеді."
    elif api_ok:
        foot = "\n\n✅ <b>Нақты уақыт</b> (интернет арқылы жаңартылды)."
    else:
        foot = (
            "\n\n⚠️ <b>Демо уақыт</b> — API қолжетімсіз. `.env` ішінде `CITY_NAME` мен "
            "`COUNTRY_NAME` тексеріңіз; кейін қайта «Уақыттар» бөлімін ашыңыз."
        )
    return _clip(
        "🕌 <b>Намаз уақыттары</b>\n\n"
        f"Қала: <b>{times['city']}</b>"
        + (f" · {times['country']}" if times.get("country") else "")
        + f"\nКүн: <b>{times['date']}</b>{extra}\n\n"
        f"🌅 Таң (фаджр): <b>{times['Фаджр']}</b>\n"
        f"☀️ Күн шығуы: <b>{times['Күн']}</b>\n"
        f"🌤 Бесін (зухр): <b>{times['Бесін']}</b>\n"
        f"🌆 Екінті (аср): <b>{times['Екінті']}</b>\n"
        f"🌇 Ақшам (мағриб): <b>{times['Ақшам']}</b>\n"
        f"🌙 Құптан (иша): <b>{times['Құптан']}</b>"
        f"{foot}"
        "\n\n<i>Әр уақыттың нақты кіріс/шығыс шегін жергілікті кестемен алған дұрыс.</i>"
    )


