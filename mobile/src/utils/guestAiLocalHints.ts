/**
 * @returns null егер сәйкес кеңес табылмаса
 */
export function getGuestLocalHint(userText: string): string | null {
  const t = userText.replace(/\s+/g, " ").trim();
  if (t.length < 2) return null;
  const out: string[] = [];
  for (const { re, line } of HINTS) {
    if (re.test(t)) {
      if (!out.includes(line)) out.push(line);
    }
  }
  if (out.length === 0) return null;
  return "Қолданба (кірусіз)\n" + out.join("\n");
}
