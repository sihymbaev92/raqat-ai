/** «Атау — қысқа мағына» пішінін бөлу */
function parseKkLine(kkLine: string): { name: string; gloss: string } {
  const parts = kkLine.split(/\s*—\s*/);
  if (parts.length >= 2) {
    return {
      name: parts[0].trim(),
      gloss: parts.slice(1).join("—").trim(),
    };
  }
  return { name: kkLine.trim(), gloss: kkLine.trim() };
}

