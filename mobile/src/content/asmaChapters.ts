/**
 * Нөмір мен қысқа жол бойынша тараулар (3 бөлім).
 */
export function getAsmaChapters(n: number, kkLine: string): AsmaChapter[] {
  const { name, gloss } = parseKkLine(kkLine);
  const seed = n * 17 + kkLine.length;
  const extra = DEEP_NOTE[n] ? `\n\n${DEEP_NOTE[n]}` : "";

  const meaningBody =
    `«${name}» — ${gloss}\n\n` +
    `${pick(T_MEANING, seed)}${extra}\n\n` +
    `Осы есімді жаттағанда және зікірде айтқанда, жүрегіңізде Алланың осы сипатына сенім мен сүйіспеншілікті оятыңыз. Есімдердің әрқайсысы басқа есімдермен бірге Алланың түпнұсқа жалғыздығын білдіреді.`;

  const quranBody =
    `${pick(T_QURAN, seed + 1)}\n\n` +
    `Кейбір аяттар мен хадистерде осы мағынаға жақын тұжырымдар кездеседі; толық тізбекті тәфсир мен хадис жинақтарынан қараңыз.`;

  const noteBody = pick(T_NOTE, seed + 2);

  const tafsirLine =
    getAsmaTafsirSentence(n) ??
    `«${name}» есімі Құран мен сүннетте сенімді жолмен келген; толық мағынасын тәфсир мен ұстаздан алыңыз.`;

  return [
    { title: kk.asma.chTafsir, body: tafsirLine },
    { title: kk.asma.chMeaning, body: meaningBody },
    { title: kk.asma.chQuran, body: quranBody },
    { title: kk.asma.chNote, body: noteBody },
  ];
}
