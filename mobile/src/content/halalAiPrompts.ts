/**
 * Халал тексеру — қысқа нұсқаулар (серверде де негізгі сценарий бар; токен аз = жылдам жауап).
 */
export function buildHalalTextPrompt(userText: string): string {
  const t = userText.trim();
  return [
    "Қазақша жауап, ## тақырыптармен. Ингредиенттерді: халал / күмәнді / харам / белгісіз деп жікте.",
    "E-кодтар, желатин, спирт, фермент, шошқа — қысқа ескер. Соңғы жол: фиқһ үкімі емес — ұстаз/ресми сертификат.",
    "",
    "=== Пайдаланушы ===",
    t,
  ].join("\n");
}

export function buildHalalImagePrompt(): string {
  return (
    "Қазақша, ## құрылымымен. Қысқа: не көрінеді; құрам белгілері; сертификат; ұсыныс. " +
    "Соңында: фиқһ үшін ұстазға жүгін."
  );
}
