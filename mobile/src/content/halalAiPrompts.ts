/**
 * Халал тексеру — сервердегі vision/chat пайдалану үшін құрылымдалған нұсқаулар.
 * Токен ұзындығын теңестіру: маңызды ережелер қысқа, пайдаланушы мәтіні толық сақталады.
 */
import { findEcodesInText, formatEcodeAppendixForPrompt } from "./halalEcodeDb";

export function buildHalalTextPrompt(userText: string): string {
  const t = userText.trim();
  const ecodeAppendix = formatEcodeAppendixForPrompt(findEcodesInText(t));
  return [
    "Рөл: тағам өнімі құрамын халал тұрғысынан ақпараттық талдау (қазақша).",
    "Шығысты мына тақырыптармен (##) реттелген Markdown түрінде бер:",
    "## Өнім немесе сұрақ",
    "## Құрам элементтерін жіктеу — әр позиция үшін: халал | күмәнді | харам | белгісіз (қысқа себеп)",
    "## E-кодтар, желатин/коллаген шығу тегі, спирт/фермент, шошқа деривативтері — болса ескер",
    "## Сертификат/халал белгісі туралы (мәтінде бар болса)",
    "## Қысқаша қорытынды және практикалық ұсыныс",
    "",
    "Ережелер: фиқһтық үкім берме; мәзһаб пен жеке жағдайды білмейсің — соңында «нақты үкім үшін білікті ұстаз немесе ресми халал ұйымына жүгін» деп жаз.",
    "Қазақстанда заң/сертификат сұрақтарында: тек қолданбалық ескертпе; нақты куәлік пен нормалар — маман ісі.",
    "Егер құрам толық емес болса, қандай ақпарат жетіспейтінін ата.",
    "",
    ...(ecodeAppendix ? [ecodeAppendix, ""] : []),
    "=== Пайдаланушы мәтіні ===",
    t,
  ].join("\n");
}

