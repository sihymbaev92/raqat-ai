TITLES_AR: tuple[str, ...] = tuple(
    ln.strip() for ln in _RAW_LINES.splitlines() if ln.strip()
)
if len(TITLES_AR) != 114:
    raise RuntimeError(f"expected 114 surah titles, got {len(TITLES_AR)}")


def backfill_quran_surah_names_sqlite(conn) -> int:
    """Әр `surah` үшін `surah_name` орнатады. Қайтарылғаны: жаңартылған жол саны (жуық)."""
    total = 0
    for surah_n, title in enumerate(TITLES_AR, start=1):
        cur = conn.execute(
            "UPDATE quran SET surah_name = ? WHERE surah = ?",
            (title, surah_n),
        )
        total += cur.rowcount or 0
    return total
